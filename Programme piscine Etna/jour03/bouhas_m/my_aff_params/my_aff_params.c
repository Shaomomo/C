void	 my_putstr(char *str)
{
  while (*str != '\0')
    {
      my_putchar(*str);
      str = str + 1;
    }
}
void	 my_aff_params(int nbr, char **arg)
{
  int	 i;
  
  i = 0;
  while (i <= nbr)
    {
      my_putstr(arg[i]);
      my_putchar('\n');
      i++;
    }
}
