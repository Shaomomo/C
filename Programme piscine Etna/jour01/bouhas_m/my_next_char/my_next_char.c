char my_next_char(char c)
{
	if (c == 'z')
	{
	  c = 'a';
	}
	else if (c == 'Z')
	  {
	    c = 'A';
	  }
	else if ((c >= 'A' && c < 'Z') ||
		 (c >= 'a' && c < 'z' ))
	  {
	    c = c + 1;
	  }
	else
	  {
	    c = ' ';
	  }
	return (c);
}

